  <h1>Welcome to the unofficial Ricochet manual.</h1>

  <section class="section-content">
    <h2 class="section-header">What is Ricochet?</h2>
    <p>
      Ricochet is a Goldsrc engine game where you hop around on platforms via
      jump pads and hit people off of them with your discs.<br />
      It's very simple in concept, but there's a few complexities which make the
      game much more exciting.<br />
      This manual intends to teach you as much as it can about Ricochet and its gameplay.<br
      /><br />
      <img src="note.gif" width="31" height="11" alt="note" /> This guide is written
      implying you know the basics of playing a Goldsrc game.
    </p>
  </section>

  <section class="section-content">
    <h2 class="section-header">The Controls</h2>
    <img src="options.png" width="427" height="182" alt="Controls" />
    <p>
      Ricochet is an average first-person Goldsrc game, using WASD and left
      click and right click.<br />
      However, there are two important things to keep in mind:<br />
      <b>1. THERE IS NO JUMP KEY.<br />2. TURN ON RAW MOUSE INPUT.</b><br />
      The first is a mistake a lot of newbies make, and the second has the game literally
      not function without it.<br />
      Once you know those, you should be good to go.<br /><br />
      All your jumping is done on the various jump arrows that reside throughout
      the map. You can airstrafe to make more slick movements, but that's out of
      the scope of this guide.
    </p>
    <div class="tips">
      <img src="tips.jpg" width="31" height="11" alt="Tip" />
      <p>
        If you want to get to a pad that's farther down, hold the forward and a
        singular strafe key and walk at it at an angle.
      </p>
    </div>
  </section>

  <section class="section-content">
    <h2 class="section-header">The Gameplay</h2>
    <img src="gameplay.png" alt="Gameplay" />
    <p>
      If you came into this expecting a rooty tooty point n' shooty, you thought
      wrong.<br />
      The gameplay is quite different, but also easy to understand in general.<br
      /><br />
      Your goal is to kill the other players by either pushing them off the edge
      with your <b>left-click</b> discs or decapitating them with your
      <b>right-click</b>
      discs.<br />
      You get a point for a direct hit kill with a push disc, and also one with a
      decapitation disc.<br /><br />
      <img src="tips.jpg" width="31" height="11" alt="Tip" />
      Decap discs fire every frame. Make use of this info if you ever find yourself
      in a situation where you can throw out multiple.<br />
    </p>
    <div class="gif-images">
      <img
        src="gameplay gifs/ricoPUSH.webp"
