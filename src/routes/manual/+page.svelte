      />
    </div>
    <small
      >Left: Killing with a regular disc. Right: Killing with a decap disc.</small
    ><br /><br />
    The real bread and butter of point gaining are <b>ricochets</b>.<br />
    If you bounce a disc off of a wall and kill a player with it, you get two points.<br
    />
    A disc can bounce for a maximum of three times before it returns to you.<br
    />
    You can get up to <b>5 points per kill</b> (maximum, no more).<br />
    You can have a maximum of three discs at play, and using a decap disc will use
    up all three.
  </section>

  <section class="section-content">
    <h2 class="section-header">The Gamemodes</h2>
    <img
      src="mapscreenshots.png"
      width="628"
      height="254"
      alt="Map Screenshots"
    />
    <p>
      There are two official gamemodes in this game; <b
        >Arena and Free-For-All.</b
      ><br /><br />
      <b>Arena</b> is a one-on-one arena fighting experience. First one to get
      three points wins, and the loser gets booted out.<br /><br />
      <b>Free-For-All</b> is what you expectâ€”bounce around in a larger map and try
      to get as many points as possible. It usually doesn't have a frag count.
    </p>
    <div class="tips">
      <img src="tips.jpg" width="31" height="11" alt="Tip" />
      <p>
        Arena is where Ricochet is most competitive. If you want to get better,
        try that mode.
      </p>
    </div>
  </section>

  <section class="section-content">
    <h2 class="section-header">The Discs and The Powerups</h2>
    <img src="discs.png" alt="Discs" />
    <p>
      You can see your discs at the top of the screen. When they glow, they are
      ready to become decap discs.<br />
      You can also collect <b>Powerups</b> which are scattered throughout the
      map and spawn frequently.<br />
      There are four types of powerups:
    </p>

    <h3>Power Discs</h3>
    <img src="powerdiscs.png" alt="Power Discs" />
    <p>
      Power Discs are collected by touching the red spiral. They allow your
      discs to decapitate when close and knock back hard when far.<br />
      <img src="tips.jpg" width="31" height="11" alt="Tip" /> Getting this powerup
      will automatically refill your discs.
    </p>

    <h3>Fast Discs</h3>
    <img src="fastdiscs.png" alt="Fast Discs" />
    <p>
      Fast Discs are collected by touching the green circles. They allow you to
      shoot speedy discs.<br />
      <img src="tips.jpg" width="31" height="11" alt="Tip" /> Getting this powerup
      will give you nine discs.
    </p>

    <h3>Triple Discs</h3>
    <img src="triplediscs.png" alt="Triple Discs" />
    <p>
      Triple Discs shoot three discs instead of one. They help you cover more
      ground.<br />
      <img src="tips.jpg" width="31" height="11" alt="Tip" /> Getting this powerup
      will automatically refill your discs.
    </p>

    <h3>Freeze Discs</h3>
    <img src="freezediscs.png" alt="Freeze Discs" />
    <p>
      Freeze Discs slow down anyone hit by them to a crawl. They can be very
      useful for making kills easier.<br />
      <img src="tips.jpg" width="31" height="11" alt="Tip" /> Getting this powerup
      will automatically refill your discs.
    </p>
